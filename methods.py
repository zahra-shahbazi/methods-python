text='Hello word my name is zahra shahbazi'

print(text.upper())      #تبدیل به حروف بزرگ 


print(text.capitalize())    #بزرگ کردن حرف اول


print(text.find('n'))       # مشخص کردن جایگاه حروف تعیین شده

print(text.count("a"))       # مشخص کرددن حروف تعیین شده که چند بار داخل جمه تکرار شده

print(text.casefold())       # کوچک کردن حروف بزرگ

print(text.lower())           # تبدیلل به حروف کوچک

print(text.title())          # بزرگ کرددن حروف اول هر کلمه